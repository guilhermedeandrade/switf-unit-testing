import Foundation

public class MathStuff {
    
    func add(x: Float, y: Float) -> Float {
        return x + y
    }
    
    func multiply(x: Float, y: Float) -> Float {
          return x * y
    }
    
    func divide(x: Float, y: Float) -> Float {
          return x / y
    }
    
}
